package com.company;

public class While {
    public static void main(String args[]) {
        showName();
        int n = 10;
        while(n >= 1){
            System.out.println("Nilai n:"+n);
            n--;
        }
    }

    private static void showName() {
        System.out.println("Nama : Yogi Prasetyawan Hadi");
        System.out.println("NIM : 191110236");
        System.out.println("----------------------");
    }
}
